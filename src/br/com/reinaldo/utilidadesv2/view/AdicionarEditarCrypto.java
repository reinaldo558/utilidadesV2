    private void addMaquinaBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMaquinaBtnSalvarActionPerformed
        
        if (addMaquinaTxtGrupo.getText() == null || "".equals(addMaquinaTxtGrupo.getText())) {
            mensagemErro("Informe o grupo dessa maquina");
            return;
        }
        
        if (addMaquinaTxtNome.getText() == null || "".equals(addMaquinaTxtNome.getText())) {
            mensagemErro("Informe o nome do servidor");
            return;
        }
        
        if (addMaquinaTxtSenha.getText() == null || "".equals(addMaquinaTxtSenha.getText())) {
            mensagemErro("Informe a senha de acesso ao servidor");
            return;
        }
        
        if (addMaquinaTxtUsuario.getText() == null || "".equals(addMaquinaTxtUsuario.getText())) {
            mensagemErro("Informe o usuario de acesso ao servidor");
            return;
        }
        
        if (addMaquinaPersonalizado.isSelected()) {
            if (addMaquinaTxtComandoPersonalizado.getText() == null || "".equals(addMaquinaTxtComandoPersonalizado.getText()) || "".equals(addMaquinaTxtComandoPersonalizado.getText().trim())) {
                mensagemErro("Informe o comando personalizado");
                return;
            }
        }
        
        final MaquinaEntity novaMaquina = new MaquinaEntity();
        novaMaquina.setDescricao(addMaquinaTxtDescricao.getText().trim());
        novaMaquina.setGrupo(addMaquinaTxtGrupo.getText().trim());
        novaMaquina.setLabel("asd");
        novaMaquina.setNome(addMaquinaTxtNome.getText().trim());
        try {
            novaMaquina.setSenha(CryptoUtil.getInstance().encrypt(VariaveisComuns.HASH, addMaquinaTxtSenha.getText().trim()));
        } catch (Exception ex) {}
        novaMaquina.setUsuario(addMaquinaTxtUsuario.getText().trim());
        novaMaquina.setUrl(addMaquinaTxtURL.getText().trim());
        novaMaquina.setComandoPersonalizado(addMaquinaTxtComandoPersonalizado.getText().trim());
        
        if (addMaquinaRadioNavegador.isSelected()) novaMaquina.setTipoTerminal(MaquinaEntity.TIPO_TERMINAL_NAVEGADOR);
        if (addMaquinaRadioPutty.isSelected()) novaMaquina.setTipoTerminal(MaquinaEntity.TIPO_TERMINAL_PUTTY);
        if (addMaquinaShell.isSelected()) novaMaquina.setTipoTerminal(MaquinaEntity.TIPO_TERMINAL_SHELL);
        if (addMaquinaPersonalizado.isSelected()) novaMaquina.setTipoTerminal(MaquinaEntity.TIPO_TERMINAL_PERSONALIZADO);
        
        if (acao == EDITAR) {
            editarMaquina(novaMaquina);
        } else if (acao == ADICIONAR) {
            adicionarMaquina(novaMaquina);
        } 
        
        AcoesComuns.procurarProperties();
        paraRecarregar.recarregarArvore();
        setVisible(false);
    }//GEN-LAST:event_addMaquinaBtnSalvarActionPerformed

