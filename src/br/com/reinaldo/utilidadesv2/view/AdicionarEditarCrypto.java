    public void preparar(int funcao, MaquinaEntity maquina, FramePrincipal pParaRecarregar) {
        maquinaOriginal = maquina;
        acao = funcao;
        paraRecarregar = pParaRecarregar;
        
        addMaquinaTxtLabel.setText("");
        addMaquinaTxtLabel.setEditable(false);
        
        addMaquinaTxtGrupo.setText("");
        addMaquinaTxtNome.setText("");
        addMaquinaTxtUsuario.setText("");
        addMaquinaTxtSenha.setText("");
        addMaquinaTxtDescricao.setText("");
        addMaquinaTxtURL.setText("");
        addMaquinaTxtComandoPersonalizado.setText("");
        addMaquinaRadioPutty.setSelected(true);
        
        if (funcao == VER || funcao == EDITAR) {
            try {
                addMaquinaTxtGrupo.setText(maquina.getGrupo());
                addMaquinaTxtNome.setText(maquina.getNome());
                addMaquinaTxtUsuario.setText(maquina.getUsuario());
                addMaquinaTxtSenha.setText(maquina.getSenhaAberta());
                addMaquinaTxtDescricao.setText(maquina.getDescricao());
                addMaquinaTxtURL.setText(maquina.getUrl());
                addMaquinaTxtComandoPersonalizado.setText(maquina.getComandoPersonalizado());
                
                if (maquina.getTipoTerminal() == MaquinaEntity.TIPO_TERMINAL_NAVEGADOR) {
                    addMaquinaRadioNavegador.setSelected(true);
                } else if (maquina.getTipoTerminal() == MaquinaEntity.TIPO_TERMINAL_SHELL) {
                    addMaquinaShell.setSelected(true);
                } else if (maquina.getTipoTerminal() == MaquinaEntity.TIPO_TERMINAL_PERSONALIZADO) {
                    addMaquinaPersonalizado.setSelected(true);
                } else {
                    addMaquinaRadioPutty.setSelected(true);
                }
                
            } catch (Exception ex) {}
        }
        if (funcao == VER) {
            addMaquinaTxtGrupo.setEditable(false);
            addMaquinaTxtNome.setEditable(false);
            addMaquinaTxtUsuario.setEditable(false);
            addMaquinaTxtSenha.setEditable(false);
            addMaquinaTxtDescricao.setEditable(false);
            addMaquinaBtnSalvar.setEnabled(false);
            addMaquinaRadioPutty.setEnabled(false);
            addMaquinaPersonalizado.setEnabled(false);
            addMaquinaShell.setEnabled(false);
            addMaquinaRadioNavegador.setEnabled(false);
            addMaquinaTxtComandoPersonalizado.setEditable(false);
            addMaquinaTxtURL.setEditable(false);
            try {
                addMaquinaTxtSenha.setText(maquina.getSenhaReduzida());
            } catch (Exception ex) {}
        } else {
            addMaquinaTxtGrupo.setEditable(true);
            addMaquinaTxtNome.setEditable(true);
            addMaquinaTxtUsuario.setEditable(true);
            addMaquinaTxtSenha.setEditable(true);
            addMaquinaTxtDescricao.setEditable(true);
            addMaquinaBtnSalvar.setEnabled(true);
            addMaquinaRadioPutty.setEnabled(true);
            addMaquinaPersonalizado.setEnabled(true);
            addMaquinaShell.setEnabled(true);
            addMaquinaRadioNavegador.setEnabled(true);
            addMaquinaTxtComandoPersonalizado.setEditable(true);
            addMaquinaTxtURL.setEditable(true);
        }
    }

