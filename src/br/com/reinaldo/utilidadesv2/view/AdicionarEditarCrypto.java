    private void editarMaquina(final MaquinaEntity novaMaquina) {
        final int op = JOptionPane.showConfirmDialog(getContentPane(), "Tem certeza que deseja os dados do servidor \"" + maquinaOriginal.getNome() + "\"?");
        if (op == JOptionPane.OK_OPTION) {
            try {
                CryptoPass.alterarMaquinaNoProperties(maquinaOriginal, novaMaquina);
            } catch (Exception ex) {}
        }
    }
    
    private void adicionarMaquina(final MaquinaEntity novaMaquina) {
        AcoesComuns.adicionarNoProperties(novaMaquina.toString());
    }
    
    
